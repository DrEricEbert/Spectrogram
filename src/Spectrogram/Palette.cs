        public static void ApplyLUT(Bitmap bmp, LUT lut)
        {
            switch (lut)
            {
                case LUT.grayscale:
                    MakeGray(bmp);
                    break;

                case LUT.qrssVdBlue:
                    MakeVdBlue(bmp);
                    return;

                case LUT.qrssVdGreen:
                    MakeVdGreen(bmp);
                    return;

                case LUT.viridis:
                    MakeViridis(bmp);
                    break;

                default:
                    throw new NotImplementedException("LUT unsupported");
            }
        }

